
fun main(args:Array<String>) {

    //Boolean is a dataType that has true or false
    var isDone:Boolean = true
    println(isDone)                     //true
    println(!isDone)                    //false

    var num1 = 3
    var num2 = 2
    println(num1 > num2)                //true
    println(num1 < num2)                //false
    println(num1 == num2)               //false
    println(num1 != num2)               //true
    println(num1 < num2 || num1>1)      //true
    println(num1 > num2 && num2 < 5)    //true

}